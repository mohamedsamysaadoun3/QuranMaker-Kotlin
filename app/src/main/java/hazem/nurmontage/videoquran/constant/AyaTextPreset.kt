package hazem.nurmontage.videoquran.constant

/**
 * Enum representing the text rendering preset for Quran ayah display.
 *
 * NONE   — No special effect, plain text.
 * OUTLINE — Text with outline/stroke effect.
 * SHADOW  — Text with drop shadow.
 * GLOW    — Text with glow/bloom effect.
 */
enum class AyaTextPreset {
    NONE,
    OUTLINE,
    SHADOW,
    GLOW
}
