package hazem.nurmontage.videoquran.constant

/**
 * Enum representing all available iPad-style frame types
 * for Quran verse display containers.
 *
 * Each value maps to a specific visual frame template used
 * in the editor to wrap Quranic text with decorative borders,
 * gradients, or themed containers.
 */
enum class IpadType {
    IPAD,
    IPAD_UNBLUR,
    IPAD_CLASSIC,
    ROUND_RECT,
    RECT,
    BOTTOM_RECT,
    BORDER,
    BLACK_LAYER,
    GRADIENT,
    BLUE_TYPE,
    MASK_BRUSH,
    IPAD_NEOMORPHIC,
    HEART,
    BATTERY,
    CASSET,
    CASSET_IMG,
    CASSET_IMG_BLUR
}
